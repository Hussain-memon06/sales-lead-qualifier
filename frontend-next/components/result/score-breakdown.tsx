import {
  Building2,
  Factory,
  Globe2,
  Layers,
  Wallet,
  ShieldAlert,
} from "lucide-react";
import { Card } from "@/components/ui/card";
import { SectionLabel } from "@/components/section-label";
import { cn } from "@/lib/utils";
import type { ICPScoreBreakdown } from "@/types/lead";

const CATEGORIES: Array<{
  key: keyof ICPScoreBreakdown;
  label: string;
  max: number;
  icon: React.ComponentType<{ className?: string }>;
}> = [
  { key: "industry_match", label: "Industry", max: 20, icon: Factory },
  { key: "company_type_match", label: "Company Type", max: 15, icon: Building2 },
  { key: "geography_match", label: "Geography", max: 15, icon: Globe2 },
  { key: "business_model_match", label: "Business Model", max: 25, icon: Layers },
  { key: "budget_signals", label: "Budget Signals", max: 25, icon: Wallet },
];

function toneFor(ratio: number) {
  if (ratio >= 0.75)
    return {
      bar: "bg-gradient-to-r from-emerald-400 to-emerald-600",
      text: "text-emerald-700",
      dot: "bg-emerald-500",
    };
  if (ratio >= 0.4)
    return {
      bar: "bg-gradient-to-r from-amber-400 to-amber-600",
      text: "text-amber-700",
      dot: "bg-amber-500",
    };
  if (ratio > 0)
    return {
      bar: "bg-gradient-to-r from-slate-300 to-slate-500",
      text: "text-slate-600",
      dot: "bg-slate-400",
    };
  return {
    bar: "bg-slate-200",
    text: "text-slate-400",
    dot: "bg-slate-300",
  };
}

export function ScoreBreakdown({
  breakdown,
  reasoning,
}: {
  breakdown: ICPScoreBreakdown;
  reasoning?: string;
}) {
  const totalTone =
    breakdown.total >= 60
      ? "text-emerald-600"
      : breakdown.total >= 40
        ? "text-amber-600"
        : "text-slate-500";

  return (
    <Card className="relative overflow-hidden p-0">
      <div
        aria-hidden
        className="pointer-events-none absolute inset-0"
        style={{
          background:
            "radial-gradient(50% 100% at 100% 0%, hsl(162 63% 42% / 0.06) 0%, transparent 60%)",
        }}
      />
      <div className="relative grid gap-0 lg:grid-cols-[1fr_320px]">
        <div className="p-6 sm:p-8">
          <SectionLabel>ICP Score Breakdown</SectionLabel>
          <p className="mt-2 text-sm text-muted-foreground max-w-md">
            Deterministic scoring computed in Python against your ICP — not
            generated by the LLM.
          </p>

          <div className="mt-7 space-y-5">
            {CATEGORIES.map(({ key, label, max, icon: Icon }) => {
              const value = breakdown[key] as number;
              const ratio = max > 0 ? value / max : 0;
              const tone = toneFor(ratio);
              return (
                <div key={key} className="space-y-2">
                  <div className="flex items-center justify-between text-sm">
                    <span className="inline-flex items-center gap-2">
                      <span
                        className={cn(
                          "flex h-6 w-6 items-center justify-center rounded-md bg-muted/70",
                          tone.text,
                        )}
                      >
                        <Icon className="h-3.5 w-3.5" />
                      </span>
                      <span className="font-medium">{label}</span>
                    </span>
                    <span className="tabular-nums text-muted-foreground">
                      <span className={cn("font-semibold", tone.text)}>
                        {value}
                      </span>
                      <span className="text-xs"> / {max}</span>
                    </span>
                  </div>
                  <div className="h-1.5 w-full overflow-hidden rounded-full bg-muted">
                    <div
                      className={cn("h-full rounded-full transition-all", tone.bar)}
                      style={{
                        width: `${Math.min(100, ratio * 100)}%`,
                      }}
                    />
                  </div>
                </div>
              );
            })}

            {breakdown.red_flags_penalty < 0 && (
              <div className="mt-2 flex items-center justify-between rounded-lg border border-rose-200/60 bg-rose-50/60 px-3 py-2.5 text-sm">
                <span className="inline-flex items-center gap-2 font-medium text-rose-700">
                  <ShieldAlert className="h-3.5 w-3.5" />
                  Red Flags Penalty
                </span>
                <span className="tabular-nums font-semibold text-rose-700">
                  {breakdown.red_flags_penalty}
                </span>
              </div>
            )}
          </div>
        </div>

        <div className="relative border-t border-border lg:border-l lg:border-t-0">
          <div
            aria-hidden
            className="pointer-events-none absolute inset-0"
            style={{
              background:
                "linear-gradient(180deg, hsl(40 33% 98%) 0%, hsl(40 30% 96%) 100%)",
            }}
          />
          <div className="relative p-6 sm:p-8 flex flex-col h-full">
            <SectionLabel>Total ICP fit</SectionLabel>
            <div className="mt-3 flex items-baseline gap-2">
              <span
                className={cn(
                  "font-display text-kpi font-semibold tabular-nums leading-none",
                  totalTone,
                )}
              >
                {breakdown.total}
              </span>
              <span className="text-base text-muted-foreground">/ 100</span>
            </div>
            {reasoning && (
              <p className="mt-5 text-[13.5px] leading-relaxed text-foreground/80 border-l-2 border-primary/30 pl-3">
                {reasoning}
              </p>
            )}
          </div>
        </div>
      </div>
    </Card>
  );
}
